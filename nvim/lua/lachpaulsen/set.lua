-- Set leader key to space.
vim.g.mapleader = " "
-- Set tabs instead of spaces and tab width to 4.
vim.opt.autoindent = true
vim.opt.expandtab = true
vim.opt.tabstop = 4
vim.opt.shiftwidth=4
vim.opt.smartindent = true
-- Set relative line numbers.
vim.opt.relativenumber = true
-- Set current line to show line number.
vim.opt.nu = true
-- Wrap lines if they roll off the screen.
vim.opt.wrap = true
-- Leave lines above and below when scrolling.
vim.opt.scrolloff = 8
